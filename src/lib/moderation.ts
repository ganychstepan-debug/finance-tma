/**
 * Модерация пользовательского ввода.
 *
 * Философия:
 * - Мы НЕ баним юзера и НЕ логируем нарушения на сервере.
 * - Просто не даём сохранить/отправить запрещённый текст.
 * - Юзер видит мягкое предупреждение и переформулирует.
 *
 * Защита от обходов (транскрипция):
 * - Латиница ↔ кириллица (naркота → наркота)
 * - Мусорные символы между буквами (н-а-р-к-о-т-а → наркота)
 * - Цифры вместо букв (нАрк0тик → наркотик)
 * - Регистр нормализуется к нижнему
 * - Множественные пробелы схлопываются
 */

// ============================================================================
// 1. Словарь транслитерации — латиница → кириллица
// ============================================================================

const LATIN_TO_CYRILLIC: Record<string, string> = {
  a: 'а', b: 'б', c: 'с', d: 'д', e: 'е', f: 'ф', g: 'г', h: 'х',
  i: 'и', j: 'ж', k: 'к', l: 'л', m: 'м', n: 'н', o: 'о', p: 'п',
  q: 'к', r: 'р', s: 'с', t: 'т', u: 'у', v: 'в', w: 'в', x: 'х',
  y: 'у', z: 'з',
}

// Цифры → похожие буквы (визуальный leetspeak)
const DIGIT_TO_LETTER: Record<string, string> = {
  '0': 'о', '1': 'и', '3': 'е', '4': 'ч', '6': 'б', '7': 'т', '8': 'в', '9': 'д',
}

/**
 * Нормализует текст для проверки на стоп-слова.
 * Убирает способы обхода: транскрипцию, разделители, регистр, цифры, мусор.
 */
export const normalize = (text: string): string => {
  let s = text.toLowerCase()

  // Латинские буквы → кириллические
  s = s.replace(/[a-z]/g, (ch) => LATIN_TO_CYRILLIC[ch] || ch)

  // Цифры → похожие буквы
  s = s.replace(/[013467890]/g, (ch) => DIGIT_TO_LETTER[ch] || ch)

  // Убираем ВСЁ что не буква русского алфавита
  // (пробелы, дефисы, знаки препинания, эмодзи, мусор)
  s = s.replace(/[^а-яё]/g, '')

  return s
}

