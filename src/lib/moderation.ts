// ============================================================================
// 3. Проверка текста на стоп-слова
// ============================================================================

export interface ModerationResult {
  ok: boolean
  category?: string      // имя категории нарушения (для UI)
  matchedRoot?: string   // какой корень сработал (для отладки)
}

/**
 * Проверяет текст на запрещённые слова.
 * Возвращает ok: true если текст чистый, иначе — информацию о нарушении.
 */
export const moderate = (text: string): ModerationResult => {
  if (!text || text.trim().length === 0) {
    return { ok: true }
  }

  const normalized = normalize(text)
  if (normalized.length < 2) {
    return { ok: true }
  }

  for (const cat of STOP_CATEGORIES) {
    for (const root of cat.roots) {
      if (normalized.includes(root)) {
        return {
          ok: false,
          category: cat.name,
          matchedRoot: root,
        }
      }
    }
  }

  return { ok: true }
}

